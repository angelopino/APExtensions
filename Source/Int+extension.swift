import UIKit

public extension Int {

    public var string: String {
        return "\(self)"
    }
    
    public var cgFloat: CGFloat {
        return CGFloat(self)
    }
    
    public var double: Double {
        return Double(self)
    }
    
    public var float: Float {
        return Float(self)
    }
}
